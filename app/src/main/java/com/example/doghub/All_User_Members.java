package com.example.doghub;

public class All_User_Members {

    String name, dogs_name, bio, dogs_age, breed, uid, url;

    public All_User_Members() {


    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDogs_name() {
        return dogs_name;
    }

    public void setDogs_name(String dogs_name) {
        this.dogs_name = dogs_name;
    }

    public String getBio() {
        return bio;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }

    public String getDogs_age() {
        return dogs_age;
    }

    public void setDogs_age(String dogs_age) {
        this.dogs_age = dogs_age;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }


}

